    public JPanel crearGraficoConsultasPorUsuario(Usuario[] usuarios, Consulta[][] matrizConsultas)
    {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        for (int i = 0; i < usuarios.length; i++)
        {
            if (usuarios[i] != null)
            {
                int numeroConsultas = 0;

                if (matrizConsultas[i] != null)
                {
                    for (Consulta consulta : matrizConsultas[i])
                    {
                        if (consulta != null)
                        {
                            numeroConsultas++;
                        }
                    }
                }

                dataset.addValue(numeroConsultas, "Consultas", usuarios[i].getNombre() + " " + usuarios[i].getApellido());
            }
        }

        JFreeChart chart = ChartFactory.createBarChart(
                "N√∫mero de Consultas por Usuario",
                "Usuario",
                "N√∫mero de Consultas",
                dataset
        );

        CategoryPlot plot = chart.getCategoryPlot();
        NumberAxis rangeAxis = (NumberAxis) plot.getRangeAxis();
        rangeAxis.setStandardTickUnits(NumberAxis.createIntegerTickUnits()); 

        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new Dimension(500, 300)); 
        return chartPanel;
    }

    public JPanel crearGraficoUsuariosPorGenero(Usuario[] usuarios)
    {
        int masculino = 0;
        int femenino = 0;

        for (Usuario usuario : usuarios)
        {
            if (usuario != null)
            {
                if ("Masculino".equalsIgnoreCase(usuario.getSexo()))
                {
                    masculino++;
                } else if ("Femenino".equalsIgnoreCase(usuario.getSexo()))
                {
                    femenino++;
                }
            }
        }

        DefaultPieDataset dataset = new DefaultPieDataset();
        dataset.setValue("Masculino (" + masculino + ")", masculino);
        dataset.setValue("Femenino (" + femenino + ")", femenino);

        JFreeChart chart = ChartFactory.createPieChart(
                "Distribuci√≥n de Usuarios por G√©nero",
                dataset,
                true,
                true,
                false
        );

        PiePlot plot = (PiePlot) chart.getPlot();
        plot.setSectionPaint("Masculino (" + masculino + ")", Color.BLUE);
        plot.setSectionPaint("Femenino (" + femenino + ")", Color.PINK);

        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new Dimension(400, 300));
        return chartPanel;
    }

